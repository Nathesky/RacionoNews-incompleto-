        <hr id="hr1">



        <p class="paragrafos">É consenso entre os especialistas que a prática regular de exercício físico é fundamental para garantir qualidade de vida e longevidade. No entanto, ainda pouco se sabe sobre como esse hábito influencia o funcionamento das células musculares.</p>

        <p class="paragrafos">Um novo estudo conduzido no Instituto de Ciências Biomédicas da Universidade de São Paulo (USP) ajuda a entender, em nível celular, como a atividade física contribui para a manutenção da aptidão física até mesmo durante o envelhecimento.</p>

        <p class="paragrafos">De acordo com o trabalho, a resposta está na mitocôndria. Esse importante componente celular, responsável por fornecer energia às células, está em constante remodelamento graças a um fenômeno chamado dinâmica mitocondrial. Essa organela pode se dividir em duas ou se unir a outra semelhante por meio de processos denominados fissão e fusão mitocondrial. A partir dessa dinâmica são coordenadas a distribuição e a função das centenas ou milhares de mitocôndrias presentes nas células musculares.</p>

        <p class="paragrafos">A pesquisa contou com o apoio da Fundação de Amparo à Pesquisa do Estado de São Paulo e os resultados foram publicados na revista The Proceedings of the National Academy of Sciences (PNAS).</p>

        <h2 class="subtitulo">Análise</h2>

        <p class="paragrafos">Por meio de experimentos com um organismo modelo bem simples, o verme de solo Caenorhabditis elegans, os pesquisadores observaram que, durante o envelhecimento, vão se acumulando nas células musculares mitocôndrias fragmentadas [que são disfuncionais].</p>

        <p class="paragrafos">Mas quando o exercício físico é praticado regularmente ao longo da vida, a frequência de mitocôndrias fusionadas aumenta, o que beneficia tanto o metabolismo mitocondrial quanto o funcionamento celular, contribuindo assim para a manutenção da fisiologia muscular durante o envelhecimento.</p>
 

        <p class="paragrafos">“No trabalho, demonstramos que, no músculo, uma única sessão de exercício físico induz rapidamente a fissão mitocondrial. E logo em seguida, após um período de recuperação, ocorre a fusão mitocondrial. Já sessões diárias ao longo da vida favorecem o aparecimento de mitocôndrias conectadas, retardando então a fragmentação mitocondrial e o declínio do condicionamento físico observados durante o envelhecimento. Dessa forma, o exercício físico e a dinâmica mitocondrial apresentaram importante associação com a manutenção da função muscular na senescência. Era a prova de conceito que faltava”, diz Julio Cesar Batista Ferreira, professor da USP e coordenador da pesquisa.</p>

        <p class="paragrafos">Em estudos anteriores, o grupo já havia demonstrado que o exercício físico atua no tratamento de doenças cardiovasculares promovendo o aparecimento de mitocôndrias fusionadas no coração.</p>

        <p class="paragrafos">Mas ainda era preciso entender como a atividade física impacta o envelhecimento de organismos saudáveis. E para isso os pesquisadores optaram por usar o nematoide C. elegans, considerado um excelente modelo experimental para estudos do envelhecimento.</p>

        <p class="paragrafos">“É muito trabalhoso e caro fazer um estudo sobre envelhecimento acompanhando indivíduos ou roedores por anos, ao longo de toda a vida. A vantagem do C. elegans é que ele apresenta uma série de similaridades com os humanos, mas tem um ciclo de vida de apenas 25 dias. Desse modo, foi possível mostrar, pela primeira vez, o que acontece com um organismo que se exercita ao longo da vida e quais são os eventos celulares críticos envolvidos no processo”, conta Ferreira.</p>

        <p class="paragrafos">Segundo o pesquisador, a dinâmica mitocondrial é importante para manter a quantidade e a qualidade das mitocôndrias na célula e, por consequência, o bom funcionamento muscular. Por meio de proteínas denominadas GTPases que “cortam” e “colam” as mitocôndrias, ocorre a fusão ou a fissão dessas organelas.</p>

        <p class="paragrafos">“Dessa forma, em condições estressoras, as proteínas removem a parte da mitocôndria que não está funcionando para ser destruída e juntam a parte funcional com outra mitocôndria. É nessa dinâmica de fissão e fusão que se dá a segregação mitocondrial e o bom funcionamento celular”.</p>

        <p class="paragrafos">Os resultados do estudo indicam que tanto a conectividade quanto o ciclo mitocondrial de fissão e fusão são essenciais para manter a aptidão física e a capacidade de resposta ao exercício durante o envelhecimento.</p>

        <h2 class="subtitulo">Protocolo de treino</h2>

        <p class="paragrafos">Um dos primeiros passos do estudo foi desenvolver um protocolo de exercício físico para os vermes. “Geralmente, esses organismos vivem em meio sólido [na natureza eles vivem na terra e, nos laboratórios de pesquisa, em gelatina]. Quando os transferimos para meio líquido, observamos que eles aumentam a frequência ondulatória associada a maior gasto energético, semelhante ao que acontece conosco humanos quando nos exercitamos”, conta Ferreira.</p>

        <p class="paragrafos">Desse modo, os pesquisadores demonstraram que a exposição diária dos vermes ao meio líquido resulta em uma série de adaptações fisiológicas e bioquímicas semelhantes às observadas em humanos e roedores exercitados.</p>

        <p class="paragrafos">“Constatamos que, quando eles se exercitam ao longo da vida, o processo de fusão e fissão mitocondrial se mantém íntegro na senescência, ao contrário dos vermes sedentários que acumulam mitocôndrias fragmentadas e disfuncionais já aos dez dias de vida, quando são considerados senis. O exercício regular faz com que o verme tenha melhor qualidade de vida, o que medimos por diferentes indicadores, como função muscular, mobilidade, ingestão de alimento e resistência a diferentes tipos de estresse ao longo da vida. Todos os indicadores estão melhores nos vermes que se exercitaram”, ressalta.</p>

        <p class="paragrafos">Segundo Ferreira, os vermes que nadaram regularmente até a fase adulta, mas se tornaram sedentários durante a velhice, também apresentaram melhores indicadores em comparação aos que sempre foram sedentários.</p>

        <p class="paragrafos">“Isso acontece porque existe uma memória celular criada pelos estímulos diários de atividade física, que é dependente do processo de fissão e fusão mitocondrial e protege esses organismos durante o envelhecimento”, explica.</p>

        <h2 class="subtitulo">Envelhecimento acelerado</h2>


        <p class="paragrafos">Por meio de técnicas de engenharia genética, os pesquisadores desligaram nos vermes os principais genes envolvidos no processo de “cortar” e “colar” mitocôndrias.</p>

        <p class="paragrafos">Essa modificação genética ocasionou um envelhecimento acelerado e, para esses organismos, o exercício passou a ter um efeito tóxico, pois não ocorre o remodelamento, segregação e remoção das mitocôndrias disfuncionais.</p>

        <p class="paragrafos">“Isso confirma a importância da dinâmica mitocondrial tanto para a senescência quanto para a prática de atividade física”, afirma Ferreira.</p>

        <p class="paragrafos">Em uma segunda parte do estudo, os pesquisadores investigaram se o aumento da longevidade é acompanhado de melhora da aptidão física nos vermes. Para isso, foram feitos experimentos com linhagens de vermes capazes de viver até 40 dias graças a alterações pontuais no genoma. Surpreendentemente, o exercício físico teve um efeito tóxico em quatro das cinco linhagens de vermes longevos testadas no estudo.</p>

        <p class="paragrafos">“Queríamos entender se o aumento da longevidade está associado ao mesmo mecanismo de melhora da aptidão física e responsividade ao exercício ao longo da vida. Essa é uma pergunta crucial, visto que a população mundial está vivendo cada vez mais. No entanto, o estudo mostrou que longevidade não necessariamente tem relação com qualidade de vida. Vale lembrar que não existe correspondente em humanos para esses vermes geneticamente modificados e que vivem quase o dobro dos vermes selvagens”, afirma Ferreira.</p>

        <h2 class="subtitulo">Sensor metabólico</h2>

        <p class="paragrafos">Apenas uma linhagem de vermes longevos (entre as cinco estudadas) apresentou melhora da aptidão física ao longo da vida.</p>

        <p class="paragrafos">Essa linhagem expressa uma enzima chamada AMPK [sigla em inglês para proteína quinase ativada por monofosfato de adenosina] constitutivamente ativa, que atua como um sensor metabólico nas células, regulando a energia e o metabolismo mitocondrial. De modo geral, a produção dessa proteína costuma diminuir com o envelhecimento.</p>

        <p class="paragrafos">“Nesse experimento, somente os vermes que tinham AMPK ativa durante toda a vida [graças a mutações feitas em laboratório] viveram e nadaram melhor por mais tempo. Entretanto, quando desligamos geneticamente as proteínas que regulam a dinâmica mitocondrial, os efeitos da AMPK foram abolidos. Nesse caso, os vermes apresentaram aptidão física reduzida e, consequentemente, declínio da função muscular na velhice”, diz Ferreira.</p>

        <p class="paragrafos">Os experimentos com a AMPK sugerem que a ativação dessa enzima pode mimetizar alguns benefícios do exercício por meio da regulação da dinâmica mitocondrial.</p>

        <p class="paragrafos">“Os mecanismos de direcionamento para otimizar a fissão e a fusão mitocondrial, bem como a ativação da AMPK, podem representar estratégias promissoras para um envelhecimento saudável, por meio de melhora nas funções bioquímicas e contráteis do músculo”, conta o pesquisador.</p>

        <p class="paragrafos">Ferreira explica que o exercício físico regular contribui para o envelhecimento saudável, pois regula os principais sistemas que alicerçam o bom funcionamento celular, entre eles a dinâmica mitocondrial.</p>

        <p class="paragrafos">“Entretanto, sabemos que a prática regular de atividade física ainda é extremamente baixa na população. Certamente, políticas públicas que utilizem informações científicas para estimular esse hábito são necessárias. Além disso, não podemos esquecer que intervenções farmacológicas capazes de controlar tais processos têm potencial para tratar diversas doenças associadas ao envelhecimento”, avalia.</p>

        <p class="paragrafos">O grupo coordenado por Ferreira no ICB-USP desenvolveu nos últimos anos uma molécula chamada SAMBA capaz de facilitar a fusão mitocondrial e, consequentemente, melhorar a qualidade de vida de animais com insuficiência cardíaca. Atualmente, o composto está em testes pré-clínicos de segurança e eficácia.</p>


