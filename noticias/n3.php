        <h1 class="titulo"> Como a prática de exercícios preserva a aptidão física no envelhecimento em nível celular </h1>

        <p class="first-letter-multiple-lines">Novo estudo conduzido no Instituto de Ciências Biomédicas da Universidade de São Paulo (USP) ajuda a entender, em nível celular, como a atividade física contribui para a manutenção da aptidão física</p> 


