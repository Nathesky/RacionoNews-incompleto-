    <p class="paragrafos">A destruição da civilização moderna devido a uma pandemia causada por um fungo ilustra o cenário da série de sucesso The Last of Us, do HBO Max. O episódio final da primeira temporada foi exibido no domingo (12) e está disponível na plataforma de streaming.</p> 

    <p class="paragrafos">A série mistura elementos de realidade e ficção ao narrar o mundo impactado pela infecção de humanos por um fungo do gênero Cordyceps. O tipo de fungo existe, mas na vida real ele é conhecido pela capacidade de parasitar insetos, como formigas e lagartas, e conseguir controlar o comportamento do hospedeiro, que se torna um tipo de “zumbi”.</p> 

    <p class="paragrafos">No entanto, ele não é capaz de infectar humanos – como acontece na série, devido à alta temperatura corporal. A narrativa ficcional cria um contexto em que essa espécie consegue evoluir ao longo do tempo devido às mudanças climáticas e ao aquecimento global, adquirindo uma capacidade de parasitar humanos e de controlá-los.</p> 

    <p class="paragrafos">“Não se tem conhecimento de espécies de fungos do gênero Cordyceps capazes de sobreviver no corpo humano e causar a morte daquela maneira vista na série. Os fungos podem sofrer mutações, mas não sabemos se uma pandemia por uma infeção fúngica pode ser causada algum dia, mas parece-me uma possibilidade bastante remota”, explica a pesquisadora do Laboratório de Desenvolvimento e Inovação (LDI), do Instituto Butantan, Ana Olívia de Souza, em comunicado.</p>

    <p class="paragrafos">Na natureza, existem mais de 400 espécies de fungos somente do gênero Cordyceps. Destes, o Ophiocordyceps unilateralis é o que age de forma mais parecida com o da série – só que em insetos.</p>

    <p class="paragrafos">“Este fungo realmente transforma a formiga em um zumbi porque secreta neurotoxinas dentro do animal, que então comprometem o funcionamento dos músculos do inseto, alterando o seu comportamento no formigueiro. Após ser infectada, a formiga fica desnorteada e começa a procurar por locais mais iluminados, e acaba saindo do formigueiro. Nesse processo, ela espalha os esporos do fungo que já tomam o seu corpo”, explica a biotecnologista Tainah Colombo Gomes, da equipe de Ana Olívia.</p>

    <h2 class="subtitulo">Como age o fungo na vida real</h2>

    <p class="paragrafos">A infecção pelo fungo – com duração de 2 a 5 dias – torna o corpo da formiga esbranquiçado, com aspecto semelhante ao mofo. Cheios de esporos microscópicos por dentro e por fora, os membros da formiga começam a ser paralisados por ação de neurotoxinas. Incapaz de se mover, a formiga morre e os resíduos de seu corpo inerte continuam sendo úteis como alimento para o fungo.</p>

    <p class="paragrafos">Este processo de reprodução do Ophiocordyceps unilateralis tem garantido a sua sobrevivência, conta o técnico sênior do Laboratório de Desenvolvimento e Inovação (LDI) Rafael Conrado.</p>

    <p class="paragrafos">“Este fungo faz com que a formiga tenha um comportamento muito específico: ao sair do formigueiro, ela se agarra a uma planta próxima e, já fixada, cresce em sua cabeça o corpo frutífero, que é o órgão reprodutor do fungo. É por onde o Cordyceps libera seus esporos, que podem infectar outras formigas ali mesmo, serem depositados no solo, ou serem levados pelo vento ou insetos para outros locais”, descreve Conrado, em comunicado.</p>

    <p class="paragrafos">Há, no entanto, uma reação por parte do formigueiro. Ao identificar que uma das formigas está infectada, a colônia se mobiliza para impedir a infecção em massa.</p>

    <p class="paragrafos">“No formigueiro, as operárias percebem o comportamento estranho das formigas que agem como zumbis e então as retiram do formigueiro para que elas não tenham contato com as outras. Elas não são racionais como nós, mas dão um jeito de eliminar as infectadas para proteger as demais”, conta a pesquisadora Ana Olívia.</p>

    <h2 class="subtitulo">O fungo “zumbi”</h2>

    <p class="paragrafos">O Ophiocordyceps unilateralis, também conhecido como “fungo zumbi”, foi descoberto pelo naturalista britânico Alfred Russel Wallace em 1859. Atualmente, a espécie é encontrada predominantemente em florestas tropicais, como a Amazônia. Ele infecta formigas-carpinteiras, da espécie Camponotus cruentatus, que se alimentam de fungos e vivem em locais quentes e úmidos, ambientes que facilitam sua proliferação.</p>

    <p class="paragrafos">O biotecnólogo Luiz Gustavo Ribeiro explica que a relação entre o fungo O. unilateralis e a formiga é fruto de um longo processo de evolução, e indica que o inseto carrega as condições necessárias para a sobrevivência do parasita.</p>

    <p class="paragrafos">“Ao longo dos anos, o fungo se adaptou ao organismo da formiga, cuja temperatura corporal é diferente da dos humanos, e o sistema nervoso bem menos complexo. Lá no começo do contato entre eles, talvez existisse uma relação benéfica, tanto para formiga quanto para o fungo, o que teria ajudado neste processo. Para se desenvolver em um corpo humano, o fungo teria de passar por muitas adaptações e ainda encontrar um corpo com sistema imune muito debilitado”, afirma Ribeiro, aluno de doutorado do LDI.</p>

    <h2 class="subtitulo">Uso em medicamentos</h2>

    <p class="paragrafos">Os especialistas do Instituto Butantan afirmam que outras espécies de fungos Cordyceps têm qualidades benéficas, inclusive para a saúde humana. Espécies como Cordyceps cicadae, Cordyceps sinensis e Cordyceps militaris, por exemplo, são amplamente usadas pela medicina tradicional chinesa.</p>

    <p class="paragrafos">“As três espécies são descritas como produtoras de compostos com diferentes propriedades farmacológicas: anti-inflamatórias, antitumorais, imunomoduladoras, nefroprotetoras e hepatoprotetoras. Os fungos do gênero Cordyceps que são benéficos têm sido considerados uma commodity rara na China”, diz Ana Olívia, pós-doutora em bioquímica pela Faculdade de Medicina de Ribeirão Preto, que estuda a aplicação farmacológica de substâncias produzidas por diferentes espécies de fungos.</p>

    <p class="paragrafos">No campo da medicina, o uso de microrganismos que podem ser nocivos na natureza como base para a produção de medicamentos é algo comum. Um dos casos mais emblemáticos é o da penicilina, descoberta de um fungo, que se tornou um dos principais antibióticos do mundo.</p>

    <p class="paragrafos">No Instituto Butantan, os cientistas Uma das linhas de pesquisa do LDI inclui um estudo que aborda a ação cicatrizante e antimicrobiana de moléculas produzidas por fungos. O objetivo é usá-las em diferentes produtos, entre os quais uma pomada para cicatrização de feridas e/ou um produto com ação antibiótica.</p>

    <p class="paragrafos">“Em nossa pesquisa, os fungos são também utilizados para desenvolvimento de nanomateriais que podem ter aplicação farmacêutica e para combater fungos prejudiciais à produção de grãos na agricultura”, conclui Ana Olívia.</p>



