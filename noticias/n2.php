        <h1 class="titulo"> Fungo de The Last of Us não é perigoso na vida real e pode ser usado em remédios </h1>

        <p class="first-letter-multiple-lines">Série mistura elementos de realidade e ficção ao narrar o mundo devastado pela infecção de humanos por um fungo do gênero Cordyceps; veja curiosidades sobre o parasita</p> 


