        <hr id="hr1">



        <p class="paragrafos">Pela primeira vez, chimpanzés foram vistos capturando insetos e os colocando em suas próprias feridas, bem como nas feridas dos outros, possivelmente como forma de medicação.</p>

        <p class="paragrafos">Esse comportamento de um animal aplicando medicação nas feridas de outro nunca havia sido observado antes e pode ser um sinal de tendências de ajuda e suporte apresentadas pelos chimpanzés, que são semelhantes à empatia em humanos, de acordo com um novo estudo.</p>

        <p class="paragrafos">Os pesquisadores testemunharam vários casos desse comportamento em uma comunidade de cerca de 45 chimpanzés no Parque Nacional Loango, no Gabão, como parte do Projeto Chimpanzé Ozouga.</p>

        <p class="paragrafos">O objetivo do projeto, liderado pelo primatologista Tobias Deschner e pela bióloga cognitiva Simone Pika, é estudar as relações e interações entre os chimpanzés, como eles caçam, usam ferramentas, comunicam e exercitam suas habilidades cognitivas. Os resultados foram publicados segunda-feira na revista Current Biology.</p>

        <p class="paragrafos">Ursos, elefantes e até abelhas já são conhecidos por se automedicarem contra parasitas e doenças.</p>

        <p class="paragrafos">“A automedicação – onde os indivíduos usam partes de plantas ou substâncias não nutritivas para combater patógenos ou parasitas – foi observada em várias espécies de animais, incluindo insetos, répteis, pássaros e mamíferos”, disse Pika em nota, autora do estudo e professora de biocognição comparativa no Instituto de Ciências Cognitivas da Universidade de Osnabrück, na Alemanha.</p>

        <p class="paragrafos">“Nossos dois parentes vivos mais próximos, os chimpanzés e bonobos, por exemplo, engolem folhas de plantas com propriedades anti-helmínticas (antiparasitárias) e mastigam folhas amargas que têm propriedades químicas para matar parasitas intestinais”.</p>

        <p class="paragrafos">Mas este é o primeiro caso registrado de animais aplicando outra matéria animal – os insetos – em feridas abertas.</p>

        <p class="paragrafos">“Os chimpanzés comem insetos, mas não sabíamos que eles os pegam e usam para tratar suas feridas”, disse Pika. “Assim, eles não só têm uma compreensão de suas espécies alimentares (plantas, insetos, macacos, pássaros, répteis), mas provavelmente também sobre as características de outras espécies animais que ajudam a agir contra lesões”.</p>

        <h2 class="subtitulo">Cuidados em uma comunidade de chimpanzés</h2>

        <p class="paragrafos">A descoberta foi feita inicialmente quando a voluntária do projeto Alessandra Mascaro assistiu a uma interação entre uma mãe chimpanzé, Suzee, e seu filho Sia, em 2019. Sia estava com um pé machucado e Suzee parecia estar cuidando dele.</p>

        <p class="paragrafos">“Percebi que ela parecia ter algo entre os lábios que ela aplicou no ferimento no pé de Sia”, disse Mascaro em nota. “Mais tarde naquela noite, eu assisti novamente aos meus vídeos e vi que Suzee estendeu a mão para pegar algo que ela colocou entre os lábios e depois diretamente na ferida aberta no pé de Sia”.</p>

        <p class="paragrafos">Cerca de uma semana depois, isso aconteceu novamente enquanto a estudante de doutorado Lara Southern observava o chimpanzé adulto Freddy fazendo algo semelhante. A equipe supôs que os chimpanzés estavam capturando pequenos insetos voadores que estavam no ar.</p>

        <p class="paragrafos">Ao longo do ano seguinte, os pesquisadores observaram e filmaram de perto todos os chimpanzés que tinham sinais de ferimentos e registraram 22 chimpanzés aplicando insetos em suas próprias feridas.</p>

        <p class="paragrafos">Então, Lara testemunhou os chimpanzés cuidando uns dos outros.</p>

        <p class="paragrafos">“Um macho adulto, Littlegrey, tinha uma ferida aberta profunda em sua canela, e Carol, uma fêmea adulta, que estava cuidando dele, de repente estendeu a mão para pegar um inseto”, disse Lara, em nota. “O que mais me impressionou foi que ela entregou esses insetos para Littlegrey, ele aplicou em sua ferida e, posteriormente, Carol e outros dois chimpanzés adultos também tocaram a ferida e moveram o inseto sobre ela. Os três chimpanzés que não são parentes pareciam realizar esses comportamentos apenas para o benefício daquele membro do grupo”.</p>

        <p class="paragrafos">Isso aconteceu novamente quando outro macho adulto também cuidou do polegar de Littlegrey, cerca de quatro meses depois.</p>

        <p class="paragrafos">Ao longo de 15 meses, a equipe catalogou 76 casos de chimpanzés usando insetos em suas feridas e nas feridas de outros, de novembro de 2019 a fevereiro de 2021.</p>

        <h2 class="subtitulo">Ajudando um ao outro</h2>

        <p class="paragrafos">Cuidar dos outros é um comportamento pró-social ou positivo que visa ajudar os outros – algo que não é frequentemente observado em animais.</p>

        <p class="paragrafos">“Para mim, isso é de tirar o fôlego, porque muitas pessoas duvidam das habilidades pró-sociais em outros animais”, disse Pika. “De repente, temos uma espécie em que realmente vemos indivíduos cuidando dos outros”.</p>

        <p class="paragrafos">“Comportamentos pró-sociais há muito tempo representam um problema para a teoria evolutiva, porque não ficou imediatamente claro por qual motivo os organismos podem ajudar os outros diante da seleção que opera no interesse de si mesmo”, escreveram os autores no estudo.</p>

        <p class="paragrafos">É difícil dizer se o que os chimpanzés estão fazendo é motivado pela empatia, mas os pesquisadores ficaram surpresos ao ver que os chimpanzés reconheceram que a maneira como tratam suas próprias feridas pode ser aplicada aos outros e se ajudaram, mesmo que não sejam parentes.</p>

        <p class="paragrafos">“Não sabemos se o comportamento observado envolve empatia”, disse Pika. “Sabemos que isso pode se qualificar como comportamento pró-social, o que significa que pode aumentar o bem-estar de outro animal – sentindo-se melhor através da atenção e carinho social, ou através de substâncias na mistura saliva-inseto que podem ser calmantes ou anti-inflamatórias. Existem exemplos de chimpanzés adotando e resgatando outros chimpanzés, o que pode envolver empatia”.</p>

        <p class="paragrafos">Os chimpanzés já demonstram há tempos que se beneficiam da cooperação ao realizar atividades como patrulhas territoriais ou caça, mas os pesquisadores continuam divididos sobre se os chimpanzés podem ser considerados pró-sociais ou empáticos.</p>

        <p class="paragrafos">“Nossas observações podem adicionar outra faceta ao debate em andamento sobre comportamentos pró-sociais e inspirar estudos futuros que investigam os comportamentos em torno do tratamento de feridas e a potencial função medicamentosa da aplicação de insetos”, escreveram os autores no estudo.</p>

        <h2 class="subtitulo">Olhando para frente</h2>

        <p class="paragrafos">É possível que os insetos que os chimpanzés estão usando tenham propriedades anti-sépticas ou anti-inflamatórias para aliviar a dor de seus ferimentos e promover a cura. Há uma longa história de humanos usando insetos para esses mesmos propósitos que remonta a 1.400 aC, disseram os pesquisadores. Eles também não podem descartar que isso seja mais um efeito placebo em potencial.</p>

        <p class="paragrafos">Pesquisas anteriores mostraram que alguns comportamentos de chimpanzés, como o uso de ferramentas, são um comportamento aprendido. Talvez medicar e ajudar o próximo seja uma dessas práticas passadas entre o grupo também.</p>

        <p class="paragrafos">Em seguida, os pesquisadores do projeto Ozouga querem identificar os insetos que os chimpanzés estão usando e acompanhar de perto quais dos animais parecem motivados a ajudar outros em sua comunidade. Eles também querem estudar os insetos para ver se eles têm propriedades farmacêuticas e determinar se o uso dos insetos ajudou a curar as feridas.</p>

        <p class="paragrafos">“É fascinante ver que, após décadas de pesquisa sobre chimpanzés selvagens, eles ainda nos surpreendem com novos comportamentos inesperados”, disse Deschner, em nota. “Nosso estudo mostra que ainda há muito a explorar e descobrir sobre nossos parentes vivos mais próximos e, portanto, precisamos nos esforçar ainda mais para protegê-los em seu habitat natural”.</p>


