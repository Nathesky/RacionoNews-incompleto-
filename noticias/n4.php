        <h1 class="titulo">Chimpanzés aplicam ‘remédio’ uns nos outros em possível demonstração de empatia</h1>

        <p class="first-letter-multiple-lines">Ao longo de 15 meses, pesquisadores no Gabão catalogaram 76 casos em que chimpanzés usaram insetos em suas próprias feridas e nas de outros</p> 


        <section>
            <img src="../assets/images/chimpas.webp" alt="Chimpaz" id="imagem-principal">
