        <hr id="hr1">



    <p class="paragrafos">A primeira tentativa de sequenciamento do código genético ocorreu há 20 anos, mas ainda havia lacunas significativas nas sequências de todos os 23 pares de cromossomos humanos.</p> 

    <p class="paragrafos">Alguns dos espaços faltantes foram preenchidos no ano passado por um grupo internacional de 100 cientistas, chamado Consórcio Telômero-para-Telômero (T2T). Mas mais de metade das sequências do cromossomo Y, o menor e mais complicado dos 46 cromossomos humanos, permaneciam desconhecidas.</p> 

    <p class="paragrafos">Agora, o mesmo grupo de pesquisadores preencheu as informações que faltavam, publicando uma sequência completa do cromossomo Y na quarta-feira (23) na revista Nature.</p> 

    <p class="paragrafos"> “Há apenas alguns anos, faltava metade do cromossomo Y humano [na sequência referência]”, disse Monika Cechova, co-autora do artigo e pós-doutoranda em engenharia biomolecular na Universidade da Califórnia, em Santa Cruz, em uma declaração.</p>

    <p class="paragrafos"> “Naquela época nem sabíamos se ele poderia ser sequenciado, era muito intrigante. Esta é realmente uma grande mudança no que é possível ou não”, acrescentou Cechova.</p>

    <h2 class="subtitulo">O que o cromossomo Y pode revelar</h2>

    <p class="paragrafos">Os humanos normalmente têm um par de cromossomos sexuais em cada célula. As pessoas designadas como sexo masculino ao nascer têm um cromossomo X e um Y, enquanto aquelas designadas como sexo feminino ao nascer têm dois cromossomos X.</p>

    <p class="paragrafos">As informações mais detalhadas fornecidas pela nova sequência de referência do Y facilitarão o estudo de condições e distúrbios ligados ao cromossomo, como a falta de produção de espermatozoides, que leva à infertilidade.</p>

    <p class="paragrafos">Pesquisas recentes sugerem que o cromossomo Y também é importante para a saúde e a longevidade, segundo Kenneth Walsh, professor de bioquímica e genética molecular da Escola de Medicina da Universidade da Virgínia, que não esteve envolvido na nova pesquisa.</p>

    <p class="paragrafos">“Foram identificados genes no cromossomo Y que se mostraram necessários para a prevenção do câncer e de doenças cardiovasculares”, pontuou Walsh por e-mail.</p>

    <p class="paragrafos">“O cromossomo Y representou a ‘matéria escura’ do genoma. Essa nova análise nos permitirá compreender melhor as regiões do cromossomo Y que possuem funções regulatórias e podem codificar mRNA e proteínas”, acrescentou.</p>

    <p class="paragrafos">Muitas pessoas começam a perder o cromossomo Y em algumas de suas células à medida que envelhecem, especialmente aquelas que sofrem rápida renovação, como as células sanguíneas.</p>

    <p class="paragrafos">Os cientistas nunca compreenderam completamente porque é isso acontece e o efeito que pode ter na saúde de uma pessoa, embora o fenômeno tenha sido associado em dois artigos científicos recentes a um aumento da gravidade do câncer da bexiga e a um maior risco de doenças cardíacas.</p>

    <p class="paragrafos">Ter uma referência genética completa do cromossomo Y pode ajudar cientistas e médicos a explorar ainda mais essa ligação potencial.</p>

    <h2 class="subtitulo">O cromossomo Y é incomumente repetitivo</h2>

    <p class="paragrafos">O cromossomo Y era particularmente difícil de decifrar porque é extraordinariamente repetitivo.</p>

    <p class="paragrafos">As quatro letras ou blocos de construção do DNA – adenina (A), citosina (C), guanina (G) e timina (T) – formam pares específicos que se unem em forma de dupla hélice. Os cromossomos são estruturas semelhantes a fios, compostas de DNA.</p>

    <p class="paragrafos">Embora todos os cromossomos humanos contenham repetições, mais de 30 milhões de letras do cromossomo Y – de um total de 62,5 milhões – são sequências repetitivas, às vezes chamadas de DNA satélite ou DNA lixo.</p>

    <p class="paragrafos">O cromossomo Y também contém palíndromos – sequências de letras iguais para frente e para trás, como um radar.</p>

    <p class="paragrafos">“Não é apenas o menor, mas também o mais complexo, por isso tem a maior quantidade de DNA repetitivo, que são como trechos de DNA que se repetem muitas, muitas vezes, às vezes em conjunto e às vezes eles podem ser intercalados com sequências únicas”, observou Cechova.</p>

    <p class="paragrafos">O DNA repetitivo complica as coisas, porque reunir dados a partir do sequenciamento genético é um pouco como tentar ler um longo livro cortado em tiras, de acordo com o Instituto Nacional de Pesquisa do Genoma Humano.</p>

    <p class="paragrafos">“Se todas as linhas do livro forem únicas, é mais fácil determinar a ordem em que as linhas vão. No entanto, se a mesma frase for repetida milhares ou milhões de vezes, a ordem original das tiras é muito menos clara”, explica o instituto, que financia o Consórcio T2T, conforme divulgado em comunicado à imprensa.</p>

    <p class="paragrafos">No caso do cromossomo Y, é como se as mesmas frases fossem repetidas durante metade do livro.</p>

    <p class="paragrafos">Os pesquisadores conseguiram obter uma leitura completa do cromossomo Y graças à nova tecnologia de sequenciamento de “leitura longa” e métodos computacionais que poderiam lidar com sequências repetitivas e transformar dados brutos em um recurso utilizável.</p>

    <p class="paragrafos">O cromossomo Y completo adiciona mais de 30 milhões de pares de bases – as sequências repetitivas – ao genoma humano de referência.</p>

    <p class="paragrafos">No início deste ano, os investigadores compilaram um “pangenoma” que incorpora o ADN de dezenas de pessoas de quase todos os continentes – essencialmente atualizando o genoma humano para o tornar mais equitativo e inclusivo.</p>

    <p class="paragrafos">Acompanhando a pesquisa publicada na mesma revista na quarta-feira, uma equipe colaboradora de cientistas reuniu cromossomos Y de 43 indivíduos do sexo masculino de 21 populações mundiais para capturar a variação genética no cromossomo Y.</p>

    <p class="paragrafos">“Estão surgindo pesquisas que mostram que a função adequada do gene do cromossomo Y é extremamente importante para a saúde geral dos homens”, disse Charles Lee, autor sênior do artigo de pesquisa que o acompanha e professor e diretor de pesquisa do Laboratório Jackson de Medicina Genômica, em um comunicado.</p>

    <p class="paragrafos">“Nosso estudo permite a inclusão do cromossomo Y completo em todos os estudos futuros ao sequenciar genomas masculinos para compreender a saúde e a doença”, finalizou.</p>
