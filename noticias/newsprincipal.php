        <h1 class="titulo"> Cientistas sequenciaram completamente o cromossomo Y pela primeira vez.</h1>

        <p class="first-letter-multiple-lines">A descoberta traz informações que podem auxiliar estudos sobre infertilidade masculina e outros problemas de saúde.</p> 


        <section>
